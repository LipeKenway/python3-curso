"""
* Python = Linguagem de Programação
* Tipo de tipagem = Dinâmica / Forte
* str => string => texto
* Strings são textos que estão dentro de aspas
"""

# Aspas Simples
print('Qualquer coisa com aspas simples')

# Aspas Duplas
print("Qualquer coisa com aspas duplas")

# Jeito mais limpo de colocar aspas dentro de um texto

# Aspas simples com Aspas duplas dentro do texto
print('Qualquer coisa com "aspas simples"')

# Aspas duplas com Aspas simples dentro de um texto
print("Qualquer coisa com 'aspas duplas'")

# Quase não se usa esses dois de baixo

# Escape
print("Qualquer coisa com \"o caractere de escape\"")

# r
print(r'Qualquer \'coisa com \'o caractere \'r')
