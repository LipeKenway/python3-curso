"""
# Tipo de dado bool (boolean)

* Ao questionar algo em um program,
* só existe duas respostas possiveis:
* Sim (True) ou Nao (False).
------------------------------------------------
* Existe varios operadores para "questionar".
* Dentre eles o ==, que eh um operador logico,
* que questiona se um valor eh igual ao outro.
"""

print(10 == 10) # Sim => True (Verdadeiro)
print(10 == 11) # Não =: False (Falso)

print(type(10 == 10)) 
print(type(10 == 11)) 

