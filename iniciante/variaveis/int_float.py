"""
# Tipos int e float

* int => Numero Inteiro
* O tipo (int) representa qualquer numero
* Positivo ou Negativo. (int) sem sinal eh considerado positivo.
---------------------------------------------------------------------
* float => Numero com ponto flutuante
* O tipo (float) representa qualquer numero
* Positivo ou Negativo com ponto flutuante
* (float) sem sinal eh considerado positivo.

* A funcao type() mostra o tipo que o Python inferiu no valor.
"""

print(type(11)) # int
print(type(-11)) # int
print(type(0)) # int

print(type(1.1)) # float
print(type(-2.4)) # float
print(type(0.0)) # float
