"""
# Operadores de comparação (Relacionais)

OP..............SIGNIFICADO..............EXEMPLO(TRUE)
> ...............Maior.....................10 > 5
>=...............Maior ou Igual............6 >= 6
< ...............Menor.....................1 < 2
<=...............Menor ou Igual............2 <= 2
==...............Igual....................'A' == 'A'
!=...............Diferente................'F' == 'C'
"""

maior = 2 > 1
maior_ou_igual = 4 >= 4
menor = 5 < 10
menor_ou_igual = 10 <= 10
igual = 'a' == 'a'
diferente = 'b' != 'c'

print(maior)
print(maior_ou_igual)
print(menor)
print(menor_ou_igual)
print(igual)
print(diferente)

